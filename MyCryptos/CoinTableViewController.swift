    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: "coinCell", for: indexPath) as? CoinTableViewCell else {
            return UITableViewCell(style: .default, reuseIdentifier: "emptyCell")
        }

        // Configure the cell...
        let coin = coins[indexPath.row]
        cell.titleLabel.text = coin.name
        cell.rankLabel.text = coin.rank
        cell.priceUSDLabel.text = coin.price_usd
        cell.change24hLabel.text = coin.percent_change_24h
        return cell
    }


